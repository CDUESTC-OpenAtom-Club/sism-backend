package com.sism.execution.domain.model.plan;

/**
 * Plan level enumeration
 * Defines the different levels of plans in the system
 */
public enum PlanLevel {
    /**
     * Strategic plan
     */
    STRATEGIC,

    /**
     * Operational plan
     */
    OPERATIONAL,

    /**
     * Comprehensive plan
     */
    COMPREHENSIVE,

    /**
     * Strategic to functional department plan
     */
    STRAT_TO_FUNC,

    /**
     * Functional department to college plan
     */
    FUNC_TO_COLLEGE
}
