package com.sism.enums;

/**
 * Milestone status enumeration
 * Defines the completion status of milestones
 */
public enum MilestoneStatus {
    /**
     * Milestone not yet started
     */
    NOT_STARTED,
    
    /**
     * Milestone in progress
     */
    IN_PROGRESS,
    
    /**
     * Milestone completed
     */
    COMPLETED,
    
    /**
     * Milestone delayed
     */
    DELAYED,
    
    /**
     * Milestone canceled
     */
    CANCELED
}
