package com.sism.enums;

/**
 * Adhoc task status enumeration
 * Defines the lifecycle status of adhoc tasks
 */
public enum AdhocTaskStatus {
    /**
     * Task is in draft state
     */
    DRAFT,
    
    /**
     * Task is active
     */
    ACTIVE,
    
    /**
     * Task is completed
     */
    COMPLETED,
    
    /**
     * Task is canceled
     */
    CANCELED
}
