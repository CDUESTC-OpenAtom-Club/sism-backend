package com.sism.enums;

/**
 * Approval action enumeration
 * Defines the possible approval actions
 */
public enum ApprovalAction {
    /**
     * Approve the report
     */
    APPROVE,
    
    /**
     * Reject the report
     */
    REJECT,
    
    /**
     * Return the report for modification
     */
    RETURN
}
